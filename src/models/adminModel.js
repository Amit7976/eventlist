import mongoose from 'mongoose';

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////


// DEFINE THE USER SCHEMA
const userSchema = new mongoose.Schema({
    email: {
        type: String,
        required: true,
    },
    password: {
        type: String,
    },
})


///////////////////////////////////////////////////////////////////////////////////////////////////////////////////


// SAVE THE MODEL
const Admin = mongoose.models?.admin || mongoose.model("admin", userSchema);


///////////////////////////////////////////////////////////////////////////////////////////////////////////////////

export default Admin;